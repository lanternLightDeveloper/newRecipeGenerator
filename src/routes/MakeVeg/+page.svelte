	<section id="understand" class="double-Block">
		<h2>1. Understand the Meat’s Role</h2>
		<p>Before substituting, figure out <strong>what the meat is doing</strong> in the recipe:</p>
		<ul>
			<li><strong>Main protein</strong> — needs a hearty, protein-rich substitute.</li>
			<li><strong>Flavor base</strong> — needs umami and texture (e.g., ground beef in sauce).</li>
			<li>
				<strong>Broth or fat source</strong> — needs depth and richness (e.g., chicken drippings).
			</li>
		</ul>
	</section>

	<section id="replacements" class="double-Block">
		<h2>2. Choose the Right Replacement</h2>
		<p>The table below maps common meats to vegetarian alternatives and quick tips.</p>

		<table class="marg_Also">
			<thead>
				<tr>
					<th>Meat Type</th>
					<th>Vegetarian Alternatives</th>
					<th>Notes / Tips</th>
				</tr>
			</thead>
			<tbody>
				<tr>
					<th>Beef</th>
					<td>Mushrooms, lentils, tempeh, TVP, seitan</td>
					<td>Add soy, miso, or tomato paste for umami.</td>
				</tr>
				<tr>
					<th>Chicken</th>
					<td>Tofu, chickpeas, jackfruit, cauliflower</td>
					<td>Poultry-style seasoning: sage, thyme, garlic, onion powder.</td>
				</tr>
				<tr>
					<th>Pork</th>
					<td>Tempeh, tofu, jackfruit, seitan</td>
					<td>Smoked paprika or liquid smoke replicates savory notes.</td>
				</tr>
				<tr>
					<th>Fish</th>
					<td>Hearts of palm, jackfruit, tofu, eggplant</td>
					<td>Use seaweed flakes or kelp for briny flavor.</td>
				</tr>
				<tr>
					<th>Ground meat</th>
					<td>Lentils, chopped mushrooms, crumbled tofu, TVP</td>
					<td>Combine items for texture variety.</td>
				</tr>
				<tr>
					<th>Bacon / Sausage</th>
					<td>Smoked tempeh, tofu bacon, mushroom “bacon”, veggie sausage</td>
					<td>Sweet-smoky spice blends help mimic flavor.</td>
				</tr>
			</tbody>
		</table>
	</section>

	<section id="umami">
		<h2>3. Add Umami & Depth</h2>
		<ul>
			<li>Soy sauce or tamari</li>
			<li>Miso paste</li>
			<li>Nutritional yeast</li>
			<li>Tomato paste</li>
			<li>Mushrooms (especially roasted)</li>
			<li>Roasted nuts or seeds</li>
		</ul>
	</section>

	<section id="maillard">
		<h2>4. Mind the Cooking Method</h2>
		<ul>
			<li>Sear or roast mushrooms, tofu, or seitan until well-browned.</li>
			<li>Use a splash of soy sauce during browning to help caramelize.</li>
			<li>Deglaze the pan with wine, broth, or vinegar to capture the fond.</li>
		</ul>
	</section>

	<section id="broth">
		<h2>5. Swap the Broth or Sauce Base</h2>
		<ul>
			<li>Vegetable broth — roasted-vegetable varieties give more body.</li>
			<li>Mushroom broth — adds deep umami.</li>
			<li>Miso-based broths — for body and savoriness.</li>
		</ul>
	</section>

	<section id="checks">
		<h2>6. Check Complementary Ingredients</h2>
		<ul>
			<li><strong>Gelatin</strong> → agar or cornstarch (for gelling).</li>
			<li><strong>Fish sauce</strong> → soy sauce + lime + seaweed (nori or kelp).</li>
			<li><strong>Anchovy paste</strong> → capers + miso or olives.</li>
		</ul>
	</section>

	<section id="examples">
		<h2>7. Examples</h2>

		<article>
			<h3>Beef Stir-Fry → Broccoli &amp; Tofu Stir-Fry</h3>
			<p>
				Swap beef for cubed tofu or tempeh. Brown the tofu, then finish with sesame oil, soy,
				garlic, and hoisin for richness.
			</p>
		</article>

		<article>
			<h3>Chicken Alfredo → Mushroom Alfredo</h3>
			<p>
				Sauté mushrooms until deeply browned, then stir into cream sauce with a splash of miso or
				soy for depth.
			</p>
		</article>

		<article>
			<h3>Pork Tacos → Jackfruit Tacos</h3>
			<p>
				Use canned young jackfruit, shred it, cook with taco seasoning and smoked paprika, then
				finish with lime and cilantro.
			</p>
		</article>
	</section>
